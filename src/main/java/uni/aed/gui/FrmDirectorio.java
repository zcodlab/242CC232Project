    private void Registrar(){
        if (jrbPersona.isSelected())    
            RegistrarPersona();
        if (jrbInteger.isSelected())
            RegistrarNumeros();
    }
    private void RegistrarPersona(){
        String nombre=jtfNombre.getText();
        int edad=Integer.parseInt(jtfEdad.getText());
        if(edad<18 || edad >70){
            JOptionPane.showMessageDialog(this, "Ingrese Edad mayor a 18 y menor a 70 años",
                    "ERROR",JOptionPane.WARNING_MESSAGE);
            return;
        }
        String genero=(String)jcbGenero.getSelectedItem();
        Persona p=new Persona(nombre,edad,genero.charAt(0));
        directorioV1.add(p);
        dlmInicial.addElement(p.toString());
        clearEntry();
        jtfNombre.requestFocus();
    }
    private void RegistrarNumeros(){
        String data=jtfData1.getText();
        if (!dlmInicial.contains(data))
            dlmInicial.addElement(data);
        jtfData1.setText("");        
    }
    private void clearEntry(){
        jtfNombre.setText(CADENA_VACIA);
        jtfEdad.setText(CADENA_VACIA);
        jcbGenero.setSelectedIndex(0);        
    }
    private void Eliminar(){
        if (dlmInicial.size()<1){
            JOptionPane.showMessageDialog(this, "La Lista se encuentra vacia",
                    "ERROR",JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(jtfNombre.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Debe consignar el valor a eliminar",
                    "ERROR",JOptionPane.WARNING_MESSAGE);
            return;
        }
        int opcion=JOptionPane.showConfirmDialog(null,
                "¿Esta seguro de eliminar el elemento de la lista?",
                "Confirmacion",
                JOptionPane.YES_NO_OPTION);
        if(opcion==JOptionPane.YES_OPTION){
            boolean eliminado=directorioV1.delete(jtfNombre.getText());
            if(!eliminado)
                JOptionPane.showMessageDialog(this, "El valor a eliminar no se encontro en la lista",
                    "ERROR",JOptionPane.WARNING_MESSAGE);
            else
                JOptionPane.showMessageDialog(this, "El valor " + jtfNombre.getText() +" elimino de la lista",
                    "ERROR",JOptionPane.WARNING_MESSAGE);
                
            
        }
            
    }
    private void ClearResult(){                
        dlmFinal.removeAllElements();                  
    }
    private void ClearAll(){        
        dlmInicial.removeAllElements();    
        jtfData1.setText(CADENA_VACIA);     
        ClearResult();
    }
